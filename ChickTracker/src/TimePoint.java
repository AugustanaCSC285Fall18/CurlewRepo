import java.awt.Point;

public class TimePoint {
	// data fields
	private int frameNum;
	private Point pt;
	
	
	public int getFrameNum() {
		return frameNum;
	}
	
	public void setFrameNum(int frameNum) {
		this.frameNum = frameNum;
	}
	
	public Point getPt() {
		return pt;
	}
	
	public void setPt(Point pt) {
		this.pt = pt;
	}
	
}
